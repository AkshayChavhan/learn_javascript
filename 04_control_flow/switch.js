// Switch statement for month comparison
const month = "march";

switch (month) {
    case "jan":
        console.log("January");
        break;
    case "feb":
        console.log("February");
        break;
    case "march":
        console.log("March");
        break;
    case "april":
        console.log("April");
        break;
    // Default case if no match is found
    default:
        console.log("Default case match");
        break;
}
